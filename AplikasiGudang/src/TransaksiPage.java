    private void tabelTransaksiMouseClicked(java.awt.event.MouseEvent evt) {
    int selectedRow = tabelTransaksi.getSelectedRow(); 
    if (selectedRow == -1) {
        return;
    }

    
    txtNamaBarang.setText(tabelTransaksi.getValueAt(selectedRow, 1).toString());
    txtJumlah.setText(tabelTransaksi.getValueAt(selectedRow, 2).toString());
    cbbTipeTransaksi.setSelectedItem(tabelTransaksi.getValueAt(selectedRow, 3).toString());
    txtHargaSatuan.setText(tabelTransaksi.getValueAt(selectedRow, 4).toString());
    txtHargaTotal.setText(tabelTransaksi.getValueAt(selectedRow, 5).toString());

    
    java.util.Date tanggal = (java.util.Date) tabelTransaksi.getValueAt(selectedRow, 6);
    dateTransaksi.setDate(tanggal);

    txtAreaKeterangan.setText(tabelTransaksi.getValueAt(selectedRow, 7).toString());
}

    
    private void tambahData() {
    String namaBarang = txtNamaBarang.getText();
    int jumlah;
    double hargaSatuan;
    String tipeTransaksi = cbbTipeTransaksi.getSelectedItem().toString();
    String keterangan = txtAreaKeterangan.getText();

    try {
        jumlah = Integer.parseInt(txtJumlah.getText());
        hargaSatuan = Double.parseDouble(txtHargaSatuan.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Jumlah dan Harga Satuan harus berupa angka!");
        return;
    }

    if (dateTransaksi.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Tanggal tidak boleh kosong!");
        return;
    }

    double hargaTotal = jumlah * hargaSatuan;
    java.sql.Date tanggalTransaksi = new java.sql.Date(dateTransaksi.getDate().getTime());

    String sql = "INSERT INTO transaksi (nama_barang, jumlah, tipe_transaksi, harga_satuan, harga_total, tanggal_transaksi, keterangan) VALUES (?, ?, ?, ?, ?, ?, ?)";

    try {
        PreparedStatement pst = DatabaseConnection.connect().prepareStatement(sql);
        pst.setString(1, namaBarang);
        pst.setInt(2, jumlah);
        pst.setString(3, tipeTransaksi);
        pst.setDouble(4, hargaSatuan);
        pst.setDouble(5, hargaTotal);
        pst.setDate(6, tanggalTransaksi);
        pst.setString(7, keterangan);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
        tampilData();
        resetForm();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
    }
}
    
    
    private void ubahData() {
    
    int selectedRow = tabelTransaksi.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah!");
        return;
    }

    
    int id_transaksi = (int) tabelTransaksi.getValueAt(selectedRow, 0); 
    String namaBarang = txtNamaBarang.getText();
    String tipeTransaksi = cbbTipeTransaksi.getSelectedItem().toString();
    int jumlah;
    double hargaSatuan;
    String keterangan = txtAreaKeterangan.getText();

    
    try {
        jumlah = Integer.parseInt(txtJumlah.getText());
        hargaSatuan = Double.parseDouble(txtHargaSatuan.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Jumlah dan Harga Satuan harus berupa angka!");
        return;
    }

    
    if (dateTransaksi.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Tanggal tidak boleh kosong!");
        return;
    }

    double hargaTotal = jumlah * hargaSatuan;
    java.sql.Date tanggalTransaksi = new java.sql.Date(dateTransaksi.getDate().getTime());

    
    String sql = "UPDATE transaksi SET nama_barang = ?, jumlah = ?, tipe_transaksi = ?, harga_satuan = ?, harga_total = ?, tanggal_transaksi = ?, keterangan = ? WHERE id_transaksi = ?";

    try {
        PreparedStatement pst = DatabaseConnection.connect().prepareStatement(sql);
        pst.setString(1, namaBarang);
        pst.setInt(2, jumlah);
        pst.setString(3, tipeTransaksi);
        pst.setDouble(4, hargaSatuan);
        pst.setDouble(5, hargaTotal);
        pst.setDate(6, tanggalTransaksi);
        pst.setString(7, keterangan);
        pst.setInt(8, id_transaksi);

        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil diubah!");

        
        tampilData();
        resetForm();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
    }
}
    
    private void hapusData() {
        int selectedRow = tabelTransaksi.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus!");
            return;
        }

        
        int idTransaksi = (int) tabelTransaksi.getValueAt(selectedRow, 0);

        
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM transaksi WHERE id_transaksi = ?";
            
            try {
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setInt(1, idTransaksi);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
                tampilData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            }
        }
    }


    
    private void tampilData() {
    String sql = "SELECT * FROM transaksi";

    try {
        PreparedStatement pst = DatabaseConnection.connect().prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();

        tabelTransaksi.setModel(new javax.swing.table.DefaultTableModel(
            new Object[][]{},
            new String[]{"ID Transaksi", "Nama Barang", "Jumlah", "Tipe Transaksi", "Harga Satuan", "Harga Total", "Tanggal Transaksi", "Keterangan"}
        ));

        while (rs.next()) {
            Object[] row = {
                rs.getInt("id_transaksi"),
                rs.getString("nama_barang"),
                rs.getInt("jumlah"),
                rs.getString("tipe_transaksi"),
                rs.getDouble("harga_satuan"),
                rs.getDouble("harga_total"),
                rs.getDate("tanggal_transaksi"),
                rs.getString("keterangan")
            };
            ((javax.swing.table.DefaultTableModel) tabelTransaksi.getModel()).addRow(row);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
    }
}
    
    private void cetakLaporan() {
    try {
        
        FileWriter writer = new FileWriter("laporan_transaksi.txt");

        
        writer.write("Laporan Transaksi Keuangan\n");
        writer.write("==========================\n");
        writer.write(String.format("%-10s %-20s %-10s %-15s %-10s %-10s %-15s %-20s\n", 
            "ID", "Nama Barang", "Jumlah", "Tipe", "Harga Satuan", "Total Harga", "Tanggal", "Keterangan"));

        
        DefaultTableModel model = (DefaultTableModel) tabelTransaksi.getModel();
        int rowCount = model.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            String idTransaksi = model.getValueAt(i, 0).toString();
            String namaBarang = model.getValueAt(i, 1).toString();
            String jumlah = model.getValueAt(i, 2).toString();
            String tipeTransaksi = model.getValueAt(i, 3).toString();
            String hargaSatuan = model.getValueAt(i, 4).toString();
            String totalHarga = model.getValueAt(i, 5).toString();
            String tanggalTransaksi = model.getValueAt(i, 6).toString();
            String keterangan = model.getValueAt(i, 7).toString();

            
            writer.write(String.format("%-10s %-20s %-10s %-15s %-10s %-10s %-15s %-20s\n", 
                idTransaksi, namaBarang, jumlah, tipeTransaksi, hargaSatuan, totalHarga, tanggalTransaksi, keterangan));
        }

        
        writer.close();
        JOptionPane.showMessageDialog(null, "Laporan berhasil dicetak ke laporan_transaksi.txt");

    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mencetak laporan: " + e.getMessage());
    }
}



