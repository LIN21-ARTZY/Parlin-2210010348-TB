private void hitungTotalHarga() {
    try {
        
        String jumlahText = txtJumlah.getText();
        String hargaSatuanText = txtHargaSatuan.getText();

        
        if (!jumlahText.isEmpty() && !hargaSatuanText.isEmpty()) {
            int jumlah = Integer.parseInt(jumlahText);
            double hargaSatuan = Double.parseDouble(hargaSatuanText);
            double hargaTotal = jumlah * hargaSatuan;

            
            txtHargaTotal.setText(String.valueOf(hargaTotal));
        } else {
            
            txtHargaTotal.setText("");
        }
    } catch (NumberFormatException e) {
        
        txtHargaTotal.setText("");
    }
}





